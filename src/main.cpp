int main(int argc, const char * argv[])
{

    const int FPS = 10;
    const int frameDelay = 1000 / FPS; // Amount of time each frame should take in milliseconds

    Uint32 frameStart;
    int frameTime;

    auto game = std::make_unique<Game>();

    game->init("GameEngine", SDL_WINDOWPOS_CENTERED, SDL_WINDOWPOS_CENTERED, 800, 640, false, true);
    game->loadMap(lvl1);

    while(game->running())
    {
        frameStart = SDL_GetTicks();

        game->handleEvents();

        // Start the search if initial inputs were given
        game->handleSearch();

        // Update and render
        game->update();
        game->render();

        // Limit frame rate
        frameTime = SDL_GetTicks() - frameStart;

        if (frameDelay > frameTime){
            SDL_Delay(frameDelay - frameTime);
        }
    }

    game->clean();

    return 0;
}