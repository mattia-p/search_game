int main(int argc, const char * argv[])
{

    const int FPS = 60;
    const int frameDelay = 1000 / FPS; // Amount of time each frame should take in milliseconds

    Uint32 frameStart;
    int frameTime;

    game = new Game;

    game->init("GameEngine", SDL_WINDOWPOS_CENTERED, SDL_WINDOWPOS_CENTERED, 800, 640, false);
    game->is_step_search = true;
    game->loadMap(lvl1);

    while(game->running())
    {
        frameStart = SDL_GetTicks();

        game->handleEvents();

        // Start the search if initial inputs were given
        game->handleSearch();

        // Update and render
        game->update();
        game->render();

        // Limit frame rate
        frameTime = SDL_GetTicks() - frameStart;

        if (frameDelay > frameTime){
            SDL_Delay(frameDelay - frameTime);
        }
    }

    game->clean();

    // Free memory allocated to game object in the heap. But now the pointer is dangling, because it still holds the same address.
    delete game;

    // Indicate that it no longer points to any object
    game = nullptr;

    return 0;
}